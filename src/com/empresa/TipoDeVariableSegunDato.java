package com.empresa;

public class TipoDeVariableSegunDato {

	public static void main(String[] args) {
		byte edad = 20;
		short numero = 32000;
		int contador = 1000;
		long distancia =100000l;
		float precio = 9.99f;
		double salario = 12345.67;
		char letra = 'A';
		boolean activo = true;
		String nombre = "Bibiana";
		
		System.out.print("byte edad = "+ edad);
		System.out.print("short numero = "+ numero);
		System.out.print("int contador = "+ contador);
		System.out.print("long distancia = "+ distancia);
		System.out.print("float precio = "+ precio);
		System.out.print("double salario = "+ salario);
		System.out.print("char letra = "+ letra);
		System.out.print("boolean activo = "+ activo);
		System.out.print("String nombre = "+ nombre);

	}

}
